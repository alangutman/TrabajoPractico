package com.RedSocial.core.model;

import com.RedSocial.core.entity.Foto;

public class FotoModel {

	public FotoModel() {
	
	}
	
	public FotoModel(long idFoto, String nombre, String ubicacion) {
		super();
		this.idFoto = idFoto;
		this.nombre = nombre;
		this.ubicacion = ubicacion;
	}

	public FotoModel(Foto foto) {
		super();
		this.idFoto = foto.getIdFoto();
		this.nombre = foto.getNombre();
		this.ubicacion = foto.getUbicacion();
	}

	private long idFoto;	
	private String nombre;
	private String ubicacion;

	public long getIdFoto() {
		return idFoto;
	}

	public void setIdFoto(long idFoto) {
		this.idFoto = idFoto;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getUbicacion() {
		return ubicacion;
	}

	public void setUbicacion(String ubicacion) {
		this.ubicacion = ubicacion;
	}
	
}
